        if (Auth::id() !== $user->id) {
            return redirect('/cuenta')->with('error', 'No puedes eliminar esta cuenta.');
        }

        // Cierra la sesiÃ³n antes de eliminar
        Auth::logout();

        // Elimina al usuario
